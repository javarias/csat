<p align="justify" style:"text-indent:50;">
<i>Hevelius</i> es un Sistema de Control de Telescopios (TCS, siglas de su nombre en 
ingl&eacute;s) Profesional dise&ntilde;ado y contruido para operar un 
telescopio aficionado.<p>

<p align="justify" style:"text-indent:50;">
El control es realizado por ACS (ALMA Commom Software),
 y la idea principal es que <i>Hevelius</i> sea usado como referente para un 
Control Gen&eacute;rico de Telescopios.
<br /><br />
<b>Los Objetivos de <i>Hevelius</i> son:</b>
<ul style="font-family: Verdana, Arial, Helvetica, sans-serif; font-size:11px; text-align:justify;">
<li> <i>Hevelius</i> ser&aacute; capaz de decidir si el objeto a observar
 est&aacute; o no en el campo visible del cielo.<br /><br />
<li> <i>Hevelius</i> proporcionar&aacute; preseting, esto es, la capacidad de
 moverse desde cierta ubicaci&oacute;n a otra de manera segura.<br /><br />
<li> <i>Hevelius</i> proporcionar&aacute; tracking. Tracking es la capacidad
del telescopio de seguir cierto objeto en el cielo, contrarestando el 
movimiento de la Tierra.<br /><br />
<li> <i>Hevelius</i> proporcionar&aacute; pointing. Pointing es la capacidad
de reconocer si el objeto, que se est&aacute; observando te&oacute;ricamente,
realmente est&aacute; siendo apuntado.<br /><br />
<li> <i>Hevelius</i> se conectar&aacute; a alg&uacute;n cat&aacute;logo de
estrellas, permitiendo observar de manera directa estrellas conocidas.<br />
<br />
<li> <i>Hevelius</i> tendr&aacute; informaci&oacute;n sobre la 
posici&oacute;n de la luna, evitando su observaci&oacute;n directa.
</ul>
</p>

<p align="justify" style:"text-indent:50;">
<i>Hevelius</i> ha sido creado con el lenguaje Java bajo los estrictos est&aacute;ndares
del framework <a href="http://www.eso.org/~almamgr/AlmaAcs/index.html">ACS</a>, lo cual
garantiza un software de calidad, &uacute;til y funcional.
</p>

