<p align="justify" style:"text-indent:50;">
<i>Hevelius</i> is a Professional Telescope Control System, 
designed and developed to operate an amateur telescope.<p>

<p align="justify" style:"text-indent:50;">
The control functionality is handled by ACS (ALMA Common Software), 
and the main objective of <i>Hevelius</i> is to be a reference for 
Generic Control of Telescopes.
<br /><br />
<b>The objectives of <i>Hevelius</i> are:</b>
<ul style="font-family: Verdana, Arial, Helvetica, sans-serif; font-size:11px; text-align:justify;">
<li> <i>Hevelius</i> will be able to decide whether an object is on visible 
field in the sky.<br /><br />
<li> <i>Hevelius</i> will provide presetting, giving the capacity of moving 
from one direction to another in a safe way.<br /><br />
<li> <i>Hevelius</i> will provide tracking, giving the capacity to follow 
an object in the sky by resisting Earth's movement.<br /><br />
<li> <i>Hevelius</i> will provide pointing, giving the capacity to know if 
the theorically observed object is really being observed.<br /><br />
<li> <i>Hevelius</i> will connect to some stars catalogue, allowing to 
easyly observe known stars and objects.<br /><br />
<li> <i>Hevelius</i> will have information about the moon's position 
avoiding direct observing.
</ul>
</p>

<p align="justify" style:"text-indent:50;">
<i>Hevelius</i> Hevelius has been created using Java language being restricted 
by the standard of <a href="http://www.eso.org/~almamgr/AlmaAcs/index.html">ACS</a> 
framework which guarantees it to be a useful quality software.
</p>

